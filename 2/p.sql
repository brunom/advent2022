),  shape_table(shape, score) as (
	VALUES ('rock', 1), ('paper', 2), ('scissors', 3)
), rule_table as (
	select v.*
	from shape_table as shape_prev
	join shape_table as shape_next
	on shape_next.score = shape_prev.score % 3 + 1
	cross join lateral (values
	(shape_next.shape, shape_prev.shape, 'lost', 0 + shape_prev.score),
	(shape_prev.shape, shape_prev.shape, 'draw', 3 + shape_prev.score),
	(shape_prev.shape, shape_next.shape, 'won' , 6 + shape_next.score)
	) as v(them, us, outcome, score)
), ABCshape(abc, shape) as (
	values ('A', 'rock'), ('B', 'paper'), ('C', 'scissors')
), XYZshape(xyz, shape) as (
	values ('X', 'rock'), ('Y', 'paper'), ('Z', 'scissors')
), XYZoutcome(xyz, outcome) as (
	values ('X', 'lost'), ('Y', 'draw'), ('Z', 'won')
), part1 as (
	select sum(score)
	from input
	join ABCshape on abc = l
	join XYZshape on xyz = r
	join rule_table on them = ABCshape.shape and us = XYZshape.shape
), part2 as (
	select sum(score)
	from input
	join ABCshape on abc = l
	join XYZoutcome on xyz = r
	join rule_table on them = ABCshape.shape and rule_table.outcome = XYZoutcome.outcome
)
